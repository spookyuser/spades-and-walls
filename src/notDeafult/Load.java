    private void deletesgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletesgActionPerformed

        //Similarly to the Load method, the delete does simply deletes he file
        String name = namecombo.getSelectedItem().toString();
        String save = jComboBox2.getSelectedItem().toString();
        SaveLoadGame slg = new SaveLoadGame();
        try {
            slg.deleteGame("saves/" + name + "/" + save); //Send path needing deletion
            JOptionPane.showMessageDialog(null, "Sucess '" + save + "' deleted"); //Print the path has been deleted
        } catch (IOException ex) {
            Logger.getLogger(Load.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deletesgActionPerformed

    private void deleteallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteallActionPerformed
        SaveLoadGame slg = new SaveLoadGame();
        try {
            slg.deleteGame("saves");
            //Deletes the Entire saves directory, this can be done because when saving a game the save method must always first
            //call a MakeDir directory
            JOptionPane.showMessageDialog(null, "All Saves deleted");
            MainMenu mn = new MainMenu();
            mn.setVisible(true);
            dispose();
        } catch (IOException ex) {
            Logger.getLogger(Load.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteallActionPerformed

