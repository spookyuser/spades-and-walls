    private void namecomboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_namecomboActionPerformed
        //Get Name combo box
        jComboBox2.removeAllItems();        //Starts wih a new list every time a combo box is called

        JComboBox jComboBox1 = (JComboBox) evt.getSource();
        String selection = (String) jComboBox1.getSelectedItem(); //Get the name
        SaveLoadGame slg = new SaveLoadGame();

        for (int i = 0; i < slg.getSubdir("saves/" + selection).length; i++) { //loop for the length of the subdirectory
            jComboBox2.addItem(slg.getSubdir("saves/" + selection)[i]);
        }
    }//GEN-LAST:event_namecomboActionPerformed

    private void loadgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadgActionPerformed
        String name = namecombo.getSelectedItem().toString(); //Get Name
        String save = jComboBox2.getSelectedItem().toString();//Get save
        SaveLoadGame slg = new SaveLoadGame();
        try {

            slg.loadGame(name, save);//Send a parameters to he Load method
        } catch (IOException ex) {
            Logger.getLogger(Load.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FontFormatException ex) {
            Logger.getLogger(Load.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(Load.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();

    }//GEN-LAST:event_loadgActionPerformed

