    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Initialise Jcombo boxes so they are fulled once the user clicks LoadGame
        //Load games are stored in the form of
        //Saves/Name/Save1 
        //By looping just through subdirectories you can see exactly how many saves each person has
        try {
            Load l = new Load();
            SaveLoadGame slg = new SaveLoadGame();
            for (int i = 0; i < slg.getSubdir("saves/").length; i++) { //Loop for the amount of subdirectories
                l.namecombo.addItem(slg.getSubdir("saves/")[i]);//Add each subdirectory 
            }

            l.setVisible(true);
            dispose();
        } catch (NullPointerException e) {

            JOptionPane.showMessageDialog(null, "You have no saved games");

        }

    }//GEN-LAST:event_jButton4ActionPerformed

