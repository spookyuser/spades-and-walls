    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //Database Is connected to
        dataBase db = new dataBase();
        db.DatabaseConnection();
        VhighScore hs = null;
        try {
            hs = new VhighScore();
        } catch (SQLException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Necessary code to correctly setup the JTable
        hs.setVisible(true);
        hs.jTable1.setFillsViewportHeight(true);
        hs.jTable1.getTableHeader().setBackground(Color.GREEN); //Game Colour
        hs.jTable1.getTableHeader().setOpaque(false);
        hs.jTable1.getTableHeader().setFont(new Font("minecraftia", Font.BOLD, 30)); //Set the Game font

        dispose();

        try {
            db.getScores(); //Get The scores
        } catch (SQLException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

